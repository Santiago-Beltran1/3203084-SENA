#Los módulos en Python son archivos con código (funciones, clases, variables)
#que se pueden importar y reutilizar en otros programas.
#Python trae módulos estándar listos para usar, por ejemplo "math".

import math

print("Ejemplo usando el módulo estándar 'math':")
numero = 25
print(f"La raíz cuadrada de {numero} es {math.sqrt(numero)}")
print(f"El seno de 90° (en radianes) es {math.sin(math.pi/2)}")
